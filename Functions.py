# defining a function called my_function() with paramater x
def my_function(x):
    return x + 1 

# Invoking the function below

# below will print out the function in two lines 

print(my_function(2)) # here x becomes 2 + 1 which = 3
# output should be 3 after invocation
print(my_function(3 + 5)) # here we will run x which is 3 
# and 3(which was x) moves back up the function of x + 1 and it now turns into 4 
# then 3 ---> 4 then the computer will add 4 + 5 
